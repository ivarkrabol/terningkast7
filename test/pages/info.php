<h2>Om foreningen</h2>
<p>Terningkast7 er en forening for deg som liker å ha det moro brettspill 
og gode venner! Vi startet opp rundt nyttår 2012/2013, og har holdt på med 
ukentlige spillkvelder siden.</p>
<p>Gjennom et tett samarbeid med Hamar Bibliotek har vi klart å samle nok 
mennesker til å fylle opp 4-5 bord med diverse brettspill samtidig, uke på uke, 
og vi har ingen planer om å roe oss ned. På grunn av det store pågangen har det 
vært snakk om å få et eget rom i det nye kulturhuset satt av til brettspilling, 
så det er mye mer moro å se frem til!</p>
<p>I mai 2013 fikk vi innvilget nok penger til å kjøpe inn 26(!) nye brettspill, 
som vi siden har gradvis inntrodusert for en spillsulten forsamling.</p>